
''' Pacote responsável pela calculadora '''


class Calc():

    def sum(self, a, b):
        '''Soma 2 números'''
        return a + b

    def sub(self, input_one, input_two):
        '''Subtrai 2 números'''
        return input_one - input_two


    def multi(self, input_one, input_two):
        '''Multiplica 2 números'''
        return input_one * input_two


    def divisoin(self, input_one, input_two):
        '''Divide 2 números'''
        return input_one / input_two
